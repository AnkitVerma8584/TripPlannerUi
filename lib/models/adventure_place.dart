class AdventurePlace {
  final int id;
  final String name, image, location, activity;
  final double rating;
  final String about, totalTime;
  final double route, highestPoint, difficulty;

  AdventurePlace({
    required this.id,
    required this.name,
    required this.image,
    required this.location,
    required this.activity,
    required this.rating,
    required this.about,
    required this.route,
    required this.highestPoint,
    required this.totalTime,
    required this.difficulty,
  });
}
